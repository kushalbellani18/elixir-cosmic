import * as React from "react";
import { Slot } from "@radix-ui/react-slot";
import { cva } from "class-variance-authority";

import { cn } from "@/lib/utils";

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-lg text-sm font-semibold ring-offset-background transition-all duration-300 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground hover:bg-primary/90",
        destructive: "bg-destructive text-destructive-foreground hover:bg-destructive/90",
        outline: "border border-input bg-background hover:bg-accent hover:text-accent-foreground",
        secondary: "bg-secondary text-secondary-foreground hover:bg-secondary/80",
        ghost: "hover:bg-accent hover:text-accent-foreground",
        link: "text-primary underline-offset-4 hover:underline",
        glass: "bg-background/20 backdrop-blur-xl border border-cosmic-pink/30 text-foreground hover:bg-background/30 hover:border-cosmic-cyan/50 hover:shadow-[0_0_30px_hsl(var(--cosmic-pink)/0.4)] hover:-translate-y-0.5",
        glassPrimary: "bg-cosmic-pink/20 backdrop-blur-xl border border-cosmic-pink/50 text-foreground hover:bg-cosmic-pink/30 hover:border-cosmic-pink hover:shadow-[0_0_40px_hsl(var(--cosmic-pink)/0.5)] hover:-translate-y-1",
        glassSecondary: "bg-cosmic-cyan/10 backdrop-blur-xl border border-cosmic-cyan/30 text-foreground hover:bg-cosmic-cyan/20 hover:border-cosmic-cyan/60 hover:shadow-[0_0_30px_hsl(var(--cosmic-cyan)/0.4)] hover:-translate-y-0.5",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 rounded-md px-3",
        lg: "h-12 rounded-lg px-8 text-base",
        xl: "h-14 rounded-xl px-10 text-lg",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  },
);

const Button = React.forwardRef(({ className, variant, size, asChild = false, ...props }, ref) => {
  const Comp = asChild ? Slot : "button";
  return <Comp className={cn(buttonVariants({ variant, size, className }))} ref={ref} {...props} />;
});
Button.displayName = "Button";

export { Button, buttonVariants };
